static void RestoreMouseCursor() {
    if (!s_fb || !s_mouse_visible) return;
    for (u32 y = 0; y < 32; ++y) {
        if (s_mouse_y + y >= s_fb_h) break;
        for (u32 x = 0; x < 24; ++x) {
            if (s_mouse_x + x >= s_fb_w) break;
            s_fb[(s_mouse_y + y) * s_fb_stride + (s_mouse_x + x)] =
                s_mouse_backing[y * 24 + x];
        }
    }
    s_mouse_visible = false;
}

static void SaveMouseCursor(u32 x0, u32 y0) {
    if (!s_fb) return;
    for (u32 y = 0; y < 32; ++y) {
        for (u32 x = 0; x < 24; ++x) {
            if (x0 + x < s_fb_w && y0 + y < s_fb_h) {
                s_mouse_backing[y * 24 + x] =
                    s_fb[(y0 + y) * s_fb_stride + (x0 + x)];
            } else {
                s_mouse_backing[y * 24 + x] = 0;
            }
        }
    }
}

// ---------------------------------------------------------------------------
// Draw one character at text cell (cx, cy) with given foreground/background
// ---------------------------------------------------------------------------
static void DrawChar(u32 cx, u32 cy, u8 ch, u32 fg, u32 bg) {
    if (!s_fb || cx >= s_cols || cy >= s_rows) return;
    u32 px = s_origin_x + cx * CHAR_W, py = s_origin_y + cy * CHAR_H;
    const u8* g = &s_font[(u32)ch * 16];
    for (u32 r = 0; r < CHAR_H; ++r) {
        if (py + r >= s_fb_h) break;
        u32* line = s_fb + (py + r) * s_fb_stride + px;
        u8 bits = g[r];
        for (u32 c = 0; c < CHAR_W; ++c) {
            if (px + c < s_fb_w)
                line[c] = (bits & (0x80u >> c)) ? fg : bg;
        }
    }
}

// ---------------------------------------------------------------------------
// Software text buffer so we can scroll and redraw
// ---------------------------------------------------------------------------
struct Cell { u8 ch, attr; };
static Cell s_cells[64][256];  // supports up to 256 cols x 64 rows (e.g. 2048x1024 at 8x16)

static void RedrawAll() {
    for (u32 r = 0; r < s_rows; ++r)
        for (u32 c = 0; c < s_cols; ++c) {
            Cell& cell = s_cells[r][c];
            DrawChar(c, r, cell.ch, AttrFg(cell.attr), AttrBg(cell.attr));
        }
}

static void SetCell(u32 r, u32 c, u8 ch, u8 attr) {
    if (r >= s_rows || c >= s_cols) return;
    s_cells[r][c] = { ch, attr };
    DrawChar(c, r, ch, AttrFg(attr), AttrBg(attr));
}

static void Scroll() {
    // Row 0 = header, row s_rows-1 = status bar -- scroll only rows 1..(s_rows-2)
    u32 last = s_rows > 2 ? s_rows - 2 : 1;
    for (u32 r = 2; r <= last; ++r)
        for (u32 c = 0; c < s_cols; ++c)
            s_cells[r-1][c] = s_cells[r][c];
    for (u32 c = 0; c < s_cols; ++c)
        s_cells[last][c] = { ' ', 0x07 };
    RedrawAll();
    s_row = last;
}

void PutChar(char ch, u8 attr) {
    EraseCursor();
    // Clamp s_row: row 0 = header, last row = status bar
    u32 max_row = s_rows > 1 ? s_rows - 2 : 1;
    if (ch == '\b') {
        if (s_col > 0) { --s_col; SetCell(s_row, s_col, ' ', 0x07); }
        DrawCursorShape(); return;
    }
    if (ch == '\r') { s_col = 0; DrawCursorShape(); return; }
    if (ch == '\n') {
        s_col = 0;
        if (++s_row > max_row) Scroll();
        DrawCursorShape(); return;
    }
    SetCell(s_row, s_col, ch, attr);
    if (++s_col >= s_cols) {
        s_col = 0;
        if (++s_row > max_row) Scroll();
    }
    DrawCursorShape();
}

// Update hardware text cursor (no-op for GOP mode; kept for ABI compat)
static void EraseCursor() {
    if (!s_cursor_visible || !s_fb) return;
    // Redraw the character underneath to remove the cursor bar
    if (s_row < s_rows && s_col < s_cols) {
        Cell& cell = s_cells[s_row][s_col];
        DrawChar(s_col, s_row, cell.ch, AttrFg(cell.attr), AttrBg(cell.attr));
    }
    s_cursor_visible = false;
}

static void DrawCursorShape() {
    if (!s_fb || s_row >= s_rows || s_col >= s_cols) return;
    // Underline cursor: last 2 pixel rows of the character cell
    u32 px = s_origin_x + s_col * CHAR_W;
    u32 py = s_origin_y + s_row * CHAR_H + CHAR_H - 2;
    u32 color = ToPixel(s_palette[7]);  // light gray
    for (u32 r = 0; r < 2 && (py + r) < s_fb_h; ++r) {
        u32* line = s_fb + (py + r) * s_fb_stride + px;
        for (u32 c = 0; c < CHAR_W; ++c)
            line[c] = color;
    }
    s_cursor_visible = true;
}


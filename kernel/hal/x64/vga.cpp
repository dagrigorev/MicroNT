// ---------------------------------------------------------------------------
// Framebuffer state (set once from BootInfo by SetFramebuffer())
// ---------------------------------------------------------------------------
static u32*  s_fb        = nullptr;
static u32   s_fb_w      = 0;   // pixels wide
static u32   s_fb_h      = 0;   // pixels tall
static u32   s_fb_stride = 0;   // pixels per scan line
static bool  s_fb_bgr    = false;// true = BGRX, false = RGBX

// Text-mode cursor (character units)
static u32 s_cols = 80;
static u32 s_rows = 25;
static u32 s_row  = 1;
static u32 s_col  = 0;

static constexpr u32 CHAR_W = 8;
static constexpr u32 CHAR_H = 16;

// ---------------------------------------------------------------------------
// CGA 16-colour palette
// ---------------------------------------------------------------------------
static const u32 s_palette[16] = {
    0x000000, 0x0000AA, 0x00AA00, 0x00AAAA,
    0xAA0000, 0xAA00AA, 0xAA5500, 0xAAAAAA,
    0x555555, 0x5555FF, 0x55FF55, 0x55FFFF,
    0xFF5555, 0xFF55FF, 0xFFFF55, 0xFFFFFF,
};

// Convert RGB value to native pixel word (RGBX or BGRX)
static u32 ToPixel(u32 rgb) {
    u8 r = (u8)(rgb >> 16), g = (u8)(rgb >> 8), b = (u8)(rgb);
    if (s_fb_bgr) return (u32)b | ((u32)g << 8) | ((u32)r << 16);
    return (u32)r | ((u32)g << 8) | ((u32)b << 16);
}
static u32 AttrFg(u8 attr) { return ToPixel(s_palette[attr & 0x0F]); }
static u32 AttrBg(u8 attr) { return ToPixel(s_palette[(attr >> 4) & 0x07]); }

// ---------------------------------------------------------------------------
// Draw one character at text cell (cx, cy) with given foreground/background
// ---------------------------------------------------------------------------
static void DrawChar(u32 cx, u32 cy, u8 ch, u32 fg, u32 bg) {
    if (!s_fb || cx >= s_cols || cy >= s_rows) return;
    u32 px = cx * CHAR_W, py = cy * CHAR_H;
    const u8* g = &s_font[(u32)ch * 16];
    for (u32 r = 0; r < CHAR_H; ++r) {
        if (py + r >= s_fb_h) break;
        u32* line = s_fb + (py + r) * s_fb_stride + px;
        u8 bits = g[r];
        for (u32 c = 0; c < CHAR_W; ++c) {
            if (px + c < s_fb_w)
                line[c] = (bits & (0x80u >> c)) ? fg : bg;
        }
    }
}

// ---------------------------------------------------------------------------
// Software text buffer so we can scroll and redraw
// ---------------------------------------------------------------------------
struct Cell { u8 ch, attr; };
static Cell s_cells[64][256];  // supports up to 256 cols x 64 rows (e.g. 2048x1024 at 8x16)

static void RedrawAll() {
    for (u32 r = 0; r < s_rows; ++r)
        for (u32 c = 0; c < s_cols; ++c) {
            Cell& cell = s_cells[r][c];
            DrawChar(c, r, cell.ch, AttrFg(cell.attr), AttrBg(cell.attr));
        }
}

static void SetCell(u32 r, u32 c, u8 ch, u8 attr) {
    if (r >= s_rows || c >= s_cols) return;
    s_cells[r][c] = { ch, attr };
    DrawChar(c, r, ch, AttrFg(attr), AttrBg(attr));
}

static void Scroll() {
    // Move rows 1..ROWS-1 up by one (row 0 = header, never scrolled)
    for (u32 r = 2; r < s_rows; ++r)
        for (u32 c = 0; c < s_cols; ++c)
            s_cells[r-1][c] = s_cells[r][c];
    for (u32 c = 0; c < s_cols; ++c)
        s_cells[s_rows-1][c] = { ' ', 0x07 };
    RedrawAll();
    s_row = s_rows - 1;
}

void PutChar(char ch, u8 attr) {
    if (ch == '\r') { s_col = 0; return; }
    if (ch == '\n') {
        s_col = 0;
        if (++s_row >= s_rows) Scroll();
        return;
    }
    SetCell(s_row, s_col, ch, attr);
    if (++s_col >= s_cols) {
        s_col = 0;
        if (++s_row >= s_rows) Scroll();
    }
}

// Update hardware text cursor (no-op for GOP mode; kept for ABI compat)
static void UpdateCursor() {}


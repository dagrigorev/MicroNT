// Called once from kernel_main with framebuffer from BootInfo
void SetFramebuffer(u64 base, u32 w, u32 h, u32 stride, u32 fmt) {
    s_fb        = reinterpret_cast<u32*>(base);
    s_fb_w      = w;
    s_fb_h      = h;
    s_fb_stride = stride;
    s_fb_bgr    = (fmt == 1);  // 1 = PixelBlueGreenRedReserved8BitPerColor
    s_cols = w / CHAR_W;  if (s_cols > 256) s_cols = 256;
    s_rows = h / CHAR_H;  if (s_rows > 64)  s_rows = 64;
}

void Init() {
    if (!s_fb) return;
    // Erase the entire GPU framebuffer to solid black first,
    // so no OVMF splash/watermark bleeds through below the text area.
    u32 black = ToPixel(0x000000);
    for (u32 y = 0; y < s_fb_h; ++y) {
        u32* row = s_fb + y * s_fb_stride;
        for (u32 x = 0; x < s_fb_w; ++x)
            row[x] = black;
    }
    // Clear text cell buffer
    for (u32 r = 0; r < s_rows; ++r)
        for (u32 c = 0; c < s_cols; ++c)
            s_cells[r][c] = { ' ', 0x07 };
    // Header bar (row 0): blue bg, bright white fg -- centered over actual width
    const char hdr[] = "*** MicroNT Kernel M24 ***";
    constexpr u32 hlen = sizeof(hdr) - 1;
    u32 hstart = (s_cols > hlen) ? (s_cols - hlen) / 2 : 0;
    for (u32 c = 0; c < s_cols; ++c)
        s_cells[0][c] = { ' ', 0x1F };
    for (u32 i = 0; i < hlen; ++i)
        s_cells[0][hstart + i] = { (u8)hdr[i], 0x1F };
    // Render everything
    RedrawAll();
    s_row = 1; s_col = 0;
}

void WriteWelcome() {
    if (!s_fb) return;
    // Write welcome info directly into text cells (rows 2-5)
    const char l1[] = "  MicroNT Microkernel  -  Build M24  ";
    const char l2[] = "  ====================================";
    const char l3[] = "  All systems online.  Ready for input.";
    const char l4[] = "  Commands: ver dir mem ps cat echo write help exit";
    auto writeln = [&](u32 row, const char* s, u8 attr) {
        for (u32 c = 0; c < s_cols && s[c]; ++c)
            SetCell(row, c, (u8)s[c], attr);
    };
    writeln(2, l1, 0x0E);  // yellow
    writeln(3, l2, 0x0A);  // green
    writeln(4, l3, 0x0F);  // bright white
    writeln(5, l4, 0x0B);  // cyan
    s_row = 7; s_col = 0;
    UpdateCursor();
}

void Print(const char* s, u8 attr) {
    for (; *s; ++s) PutChar(*s, attr);
}

// Print a [USER] line: always at column 0 of s_row
void PrintUser(const char* buf, usize len) {
    // Write directly into cells at s_row, bypassing s_col entirely
    if (s_row < 1) s_row = 1;
    if (s_row >= s_rows) Scroll();
    u32 col = 0;
    const char* pre = "[USER] ";
    for (u32 i = 0; pre[i] && col < s_cols; ++i, ++col)
        SetCell(s_row, col, (u8)pre[i], 0x0B);  // cyan
    for (u32 i = 0; i < len && buf[i] && buf[i] != '\n' && buf[i] != '\r' && col < s_cols; ++i, ++col)
        SetCell(s_row, col, (u8)buf[i], 0x0F);  // bright white
    // Blank rest of row
    for (; col < s_cols; ++col) SetCell(s_row, col, ' ', 0x07);
    ++s_row;
    s_col = 0;
    UpdateCursor();
}


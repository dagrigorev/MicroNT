void Init() {
    if (!s_fb) return;
    s_origin_x = 0;
    s_origin_y = 0;
    s_cols = s_fb_w / CHAR_W;  if (s_cols > 256) s_cols = 256;
    s_rows = s_fb_h / CHAR_H;  if (s_rows > 64)  s_rows = 64;
    s_cursor_visible = false;
    // Erase the entire GPU framebuffer to solid black first,
    // so no OVMF splash/watermark bleeds through below the text area.
    u32 black = ToPixel(0x000000);
    for (u32 y = 0; y < s_fb_h; ++y) {
        u32* row = s_fb + y * s_fb_stride;
        for (u32 x = 0; x < s_fb_w; ++x)
            row[x] = black;
    }
    // Clear text cell buffer
    for (u32 r = 0; r < s_rows; ++r)
        for (u32 c = 0; c < s_cols; ++c)
            s_cells[r][c] = { ' ', 0x07 };
    // Header bar (row 0): blue bg, bright white fg -- centered over actual width
    const char hdr[] = "*** MicroNT Kernel M28 ***";
    constexpr u32 hlen = sizeof(hdr) - 1;
    u32 hstart = (s_cols > hlen) ? (s_cols - hlen) / 2 : 0;
    for (u32 c = 0; c < s_cols; ++c)
        s_cells[0][c] = { ' ', 0x1F };
    for (u32 i = 0; i < hlen; ++i)
        s_cells[0][hstart + i] = { (u8)hdr[i], 0x1F };
    // Render everything
    RedrawAll();
    s_row = 1; s_col = 0;
}

// ClearScreen: fast clear for the 'clear' shell command.

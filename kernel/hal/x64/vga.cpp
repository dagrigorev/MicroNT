// Load the 8x16 font into VGA plane 2.
// VGA stores fonts with 32-byte slots per character (even for 16-row fonts).
// Without this UEFI/OVMF clears plane 2, so every glyph renders as blank.
static void LoadFont() {
    // Switch sequencer to write only plane 2, sequential addressing
    out8(0x3C4, 0x02); out8(0x3C5, 0x04);   // Map Mask: plane 2 only
    out8(0x3C4, 0x04); out8(0x3C5, 0x06);   // Memory Mode: extended, no odd/even

    // Graphics controller: read plane 2, sequential addressing at A0000
    out8(0x3CE, 0x04); out8(0x3CF, 0x02);   // Read Map Select: plane 2
    out8(0x3CE, 0x05); out8(0x3CF, 0x00);   // Mode: write mode 0, read mode 0
    out8(0x3CE, 0x06); out8(0x3CF, 0x00);   // Misc: A0000-BFFFF, sequential

    // Copy font glyphs to plane 2.
    // VGA ALWAYS uses 32-byte slots per character regardless of actual height.
    // Char N's glyph starts at 0xA0000 + N*32; rows 16-31 are unused/zero.
    volatile u8* fp = reinterpret_cast<volatile u8*>(0xA0000ULL);
    for (u32 ch = 0; ch < 256; ++ch) {
        for (u32 row = 0; row < 32; ++row) {
            fp[ch * 32 + row] = (row < 16) ? s_font_8x16[ch * 16 + row] : 0;
        }
    }

    // Restore sequencer and graphics controller to text-mode settings
    out8(0x3C4, 0x02); out8(0x3C5, 0x03);   // Map Mask: planes 0+1
    out8(0x3C4, 0x04); out8(0x3C5, 0x02);   // Memory Mode: extended, odd/even
    out8(0x3CE, 0x04); out8(0x3CF, 0x00);   // Read Map Select: plane 0
    out8(0x3CE, 0x05); out8(0x3CF, 0x10);   // Mode: odd/even read
    out8(0x3CE, 0x06); out8(0x3CF, 0x0E);   // Misc: B8000, odd/even
}

void Init() {
    SetTextMode();  // force VGA back to text mode after UEFI GOP
    LoadFont();       // restore font glyphs (UEFI clears VGA plane 2)
    // Clear screen (light-gray on black)
    for (u32 i = 0; i < ROWS * COLS; ++i) s_buf[i] = 0x0720;
    // Header bar: blue bg, bright white fg
    const char* hdr = " MicroNT Kernel ";
    u32 hlen = 0; while (hdr[hlen]) ++hlen;
    u32 hstart = (COLS - hlen) / 2;
    for (u32 i = 0; i < COLS; ++i) s_buf[i] = 0x1F20;
    for (u32 i = 0; i < hlen; ++i) s_buf[hstart + i] = 0x1F00 | (u8)hdr[i];
    s_row = 1; s_col = 0;
    UpdateCursor();
}

// Print a [USER] line in cyan
void PrintUser(const char* buf, usize len) {
    const char* prefix = "[USER] ";
    Print(prefix, 0x0B);     // cyan
    for (usize i = 0; i < len && buf[i] && buf[i] != '\r'; ++i) {
        if (buf[i] == '\n') break;
        PutChar(buf[i], 0x0F);
    }
    PutChar('\n', 0x07);
    UpdateCursor();
}


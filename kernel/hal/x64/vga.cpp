static u32*  s_fb        = nullptr;
static u32   s_fb_w      = 0;   // pixels wide
static u32   s_fb_h      = 0;   // pixels tall
static u32   s_fb_stride = 0;   // pixels per scan line
static bool  s_fb_bgr    = false;// true = BGRX, false = RGBX

// Text-mode cursor (character units)
static u32 s_cols = 80;
static u32 s_rows = 25;

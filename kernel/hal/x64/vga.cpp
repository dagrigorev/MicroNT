// Show cursor at current position (call after any operation that moves the cursor)
void UpdateCursor() { EraseCursor(); DrawCursorShape(); }

// Toggle cursor blink -- call from PIT handler every ~50 ticks (500 ms)
void BlinkCursor() {
    if (s_cursor_visible) EraseCursor();
    else DrawCursorShape();
}

// Called once from kernel_main with framebuffer from BootInfo
void SetFramebuffer(u64 base, u32 w, u32 h, u32 stride, u32 fmt) {
    s_fb        = reinterpret_cast<u32*>(base);
    s_fb_w      = w;
    s_fb_h      = h;
    s_fb_stride = stride;
    s_fb_bgr    = (fmt == 1);  // 1 = PixelBlueGreenRedReserved8BitPerColor
    s_origin_x = 0;
    s_origin_y = 0;
    s_cols = w / CHAR_W;  if (s_cols > 256) s_cols = 256;
    s_rows = h / CHAR_H;  if (s_rows > 64)  s_rows = 64;
}

bool GetFramebufferInfo(FramebufferInfo& info) {
    if (!s_fb || s_fb_w == 0 || s_fb_h == 0) return false;

    info.Width = s_fb_w;
    info.Height = s_fb_h;
    info.Stride = s_fb_stride;
    info.Format = s_fb_bgr ? 1u : 0u;
    return true;
}

static void ResetTextSurface(u32 x, u32 y, u32 w, u32 h) {
    s_origin_x = x;
    s_origin_y = y;
    s_cols = w / CHAR_W; if (s_cols > 256) s_cols = 256;
    s_rows = h / CHAR_H; if (s_rows > 64)  s_rows = 64;
    if (s_cols < 20) s_cols = 20;
    if (s_rows < 8)  s_rows = 8;

    for (u32 r = 0; r < s_rows; ++r)
        for (u32 c = 0; c < s_cols; ++c)
            s_cells[r][c] = { ' ', 0x07 };
    s_row = 1;
    s_col = 0;
    s_cursor_visible = false;
}


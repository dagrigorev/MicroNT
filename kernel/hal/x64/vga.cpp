// Convert RGB value to native pixel word (RGBX or BGRX)
static u32 ToPixel(u32 rgb) {
    u8 r = (u8)(rgb >> 16), g = (u8)(rgb >> 8), b = (u8)(rgb);
    if (s_fb_bgr) return (u32)b | ((u32)g << 8) | ((u32)r << 16);
    return (u32)r | ((u32)g << 8) | ((u32)b << 16);
}
static u32 AttrFg(u8 attr) { return ToPixel(s_palette[attr & 0x0F]); }
static u32 AttrBg(u8 attr) { return ToPixel(s_palette[(attr >> 4) & 0x07]); }

